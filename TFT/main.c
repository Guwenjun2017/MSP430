void draw_sin_cos(uint *sin, uint *cos)
{
    uint i=0;
    for(;i<=256 ;i++)
    {
        TFT_draw_point(0+sin[i]/1.5, 100+cos[i]/1.5, BLUE);
    }
}

void Port_init()
{
    P4SEL = 0x00;
    P4DIR = 0xff;
    P5SEL = 0x00;
    P5DIR |= BIT0 + BIT1 + BIT3 + BIT5 + BIT6 + BIT7;
}

void Clock_init()
{
    unsigned int i;
    BCSCTL1 &= ~XT2OFF;
    do
    {
        IFG1 &= ~OFIFG;
        for(i=0xff; i>0; i--);
    }while((IFG1&OFIFG));
    BCSCTL2 |= SELM_2 + SELS;
}
/**
 * main.c
 */
void main(void)
{
	WDTCTL = WDTPW | WDTHOLD;	// stop watchdog timer
	Clock_init();
	Port_init();
	TFT_init();

	TFT_clear(WHITE);
	delay_ms(10);
	while(1)
	{
	    draw_sin_cos(sin, cos);
	}
}
